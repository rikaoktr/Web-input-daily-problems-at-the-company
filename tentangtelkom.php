	<h2>PT.Telkom</h2>
	<h3>Riwayat Singkat PT.TELKOM</h3>
	</hgroup>
	<p>Telkom merupakan BUMN yang bergerak di bidang jasa layanan telekomunikasi dan jaringan di wilayah Indonesia dan karenanya tunduk pada hukum dan peraturan yang berlaku di Indonesia. Dengan statusnya sebagai Perusahaan milik negara yang sahamnya diperdagangkan di bursa saham, pemegang saham mayoritas Perusahaan adalah Pemerintah Republik Indonesia sedangkan sisanya dikuasai oleh publik. Saham Perusahaan diperdagangkan di BEI, NYSE, LSE dan Public Offering Without Listing (“POWL”) di Jepang. Riwayat singkat Telkom dari tahun ke tahun dapat dilihat pada bagian “Sejarah Panjang Menempa Kami”.</p>
